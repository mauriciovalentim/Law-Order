function imgGet(file, qnt){
    const arrayImgs = []
    for (let i = 0; i <= qnt; i++){
        let newImg
        newImg = `../animations/${file}/sprite_${i}.png`
        arrayImgs.push(newImg)
    }
    return arrayImgs
}

export function getLevels(level = 0){
    if (level == 0){
        return levels
    }
    else {
        for (let i = 1; i <= levels.length; i++){
            if (levels[i].id == level){
                return levels[i]
            }
        }
    }
}